        <h3>What is Jamii?</h3>
        <p>In an era of so many choices we need to choose a name that will be memorable, relatable and that will be
            differentiated and captivate users/consumers and eventually capture mind share. However, there is no
            instruction manual for building a brand. The name is the first thing that people hear when you tell them
            about your organisation or services. Good brand names are seldom arbitrary. They are carefully curated
            ambassadors of a brand personality that convey its position, tone, and values. A name lacking in vision,
            attention to detail, relevance and creativity can be severely detrimental to a brand image in front of
            consumers as well as potential investors. The name is also a way to create an emotional connect with
            users/consumers, therefore, it should be something that appeals to their sensibilities and something theyâ€™d
            like to be associated with for times to come.The name that we propose for this platform need to appeal to our
            primary target market and must be easily remembered. Our recommendation is the Swahili word: Jamii.
            It can mean societies/ communities and is used in many sentences constructed around communities, etc. As the
            Jamii name domain is already registered we propose the name Jamii, and the domain to be Jamiitrade. africa or
            jamiiafcfta.africa if allowed by the AfCFTA secretariat.</p>

        <h3>Goal</h3>
        <ul>
            <li>Provide basic and standard information on the AfCFTA that is easily accessible and easy to navigate.</li>
            <li>Grow intra-Africa trade as a result of available, easily understood and accessible trade information.</li>
            <li>A potential source of intelligence and insights that provide easily understood information on trade
                policies and information that is updates on ongoing basis as the AfCFTA progresses.</li>
            <li>A platform and tool that can provide information on access to markets for SMEs, SMMEs and informal traders</li>
            <li>A platform and tool that can in time become self-sustaining through the involvement of commercial entities
                that want to access the database.</li>
        </ul>
